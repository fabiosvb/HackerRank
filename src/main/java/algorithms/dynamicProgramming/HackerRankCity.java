package algorithms.dynamicProgramming;

import java.util.Scanner;

public class HackerRankCity {

	public static void main (String[] arg) {
		Scanner in = new Scanner(System.in);
		int n = in.nextInt();
		int[] arr = new int[n];
		for (int i = 0; i < n; i++) {
			arr[i] = in.nextInt();
		} 
		
		in.close();
	}

	
}

